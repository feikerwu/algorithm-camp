/**
 * @param {number[]} nums
 */
var NumArray = function (nums) {
  this.data = nums;
  let result = 0;
  this.preSums = nums.map(item => (result += item));
};

/**
 * @param {number} i
 * @param {number} j
 * @return {number}
 */
NumArray.prototype.sumRange = function (i, j) {
  const { preSums, data } = this;
  return preSums[j] - preSums[i] + data[i];
};

