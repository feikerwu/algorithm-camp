  function find(v) {
    if (v === father[v]) {
      return v;
    }
    father[v] = find(father[v]);
    return father[v];
  }
  function union(x, y) {
    if (find(x) !== find(y)) {
      father[x] = find(y);
    }
  }

  for (let i = 0; i < graph.length; i++) {
    for (let j = 0; j < graph[0].length; j++) {
      if (graph[i][j]) {
        union(i, j);
      }
    }
  }

  initial.sort((a, b) => a - b);

  let counts = graph.reduce((acc, cur, index) => {
    let root = find(index);
    if (!acc[root]) {
      acc[root] = 0;
    }
    acc[root]++;
    return acc;
  }, {});

